# 赋值名称列表
names = ['qiqi', 'fengfeng', 'nanan', 'xiaoming']

# 分别打印列表中的名字
print(names[0])
print(names[1])
print(names[2])
print(names[3])

# 赋值问候语
message = "Happy New year！"

# 用人名在前的方式，打印各个人的问候
print(names[0].title() + '! ' + message)
print(names[1].title() + '! ' + message)
print(names[2].title() + '! ' + message)
print(names[3].title() + '! ' + message)
